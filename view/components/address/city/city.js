'use strict';

arikaim.component.onLoaded(function() {
    var dataField = $('.city-dropdown').attr('data-field');
    
    $('.city-dropdown').dropdown({
        apiSettings: {     
            on: 'now',      
            url: arikaim.getBaseUrl() + '/api/address/city/list/' + dataField + '/{query}',   
            cache: false        
        },       
        filterRemoteData: false         
    });
});